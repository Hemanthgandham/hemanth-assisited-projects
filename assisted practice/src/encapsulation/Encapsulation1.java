package encapsulation;

public class Encapsulation1 {


	
	    private String Name; 
	    private String sex; 
	    private int Age;
	    
	    public int getAge()  
	    { 
	      return Age; 
	    } 
	    public String getName()  
	    { 
	      return Name; 
	    } 
	    public String getSex()  
	    { 
	       return sex; 
	    } 
	   
	    public void setName(String newName) 
	    { 
	      Name = newName; 
	    } 
	    public void setSex(String newSex) 
	    { 
	      Name = newSex;
	    }
	    public void setAge( int newAge) 
		    { 
		      Age = newAge; 
	    } 
	}
	